#Stores two decimal numbers as variables
first_num = 2.4
second_num = 6.3

#Stores operations using the two variables
num_addition = first_num + second_num
num_subtraction = first_num - second_num
num_multiplication = first_num * second_num 
num_division = second_num / first_num

#Prints the results of the operations
print(str(first_num) + " plus " + str(second_num) + " equals " + str(num_addition))
print(str(first_num) + " minus " + str(second_num) + " equals " + str(num_subtraction))
print(str(first_num) + " times " + str(second_num) + " equals " + str(num_multiplication))
print(str(second_num) + " divided by " + str(first_num) + " equals " + str(num_division))

